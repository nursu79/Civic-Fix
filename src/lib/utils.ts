/**
 * Translate database notification messages to the current locale
 */
export function translateNotification(message: string, locale: string, t: any) {
  if (!message) return "";
  
  // Regex patterns for English messages generated by triggers
  const patterns = [
    {
      regex: /^Your issue "(.*)" is now (.*)\.$/,
      key: 'status_change_owner',
      map: (match: any) => ({ issue: match[1], status: match[2] })
    },
    {
      regex: /^The issue "(.*)" you are following is now (.*)\.$/,
      key: 'status_change_follower',
      map: (match: any) => ({ issue: match[1], status: match[2] })
    },
    {
      regex: /^(.*) upvoted your issue: "(.*)"$/,
      key: 'upvote',
      map: (match: any) => ({ actor: match[1], issue: match[2] })
    },
    {
      regex: /^Someone upvoted your issue: "(.*)"$/,
      key: 'generic_upvote',
      map: (match: any) => ({ issue: match[1] })
    },
    {
       regex: /^(.*) followed your issue: "(.*)"$/,
       key: 'follow',
       map: (match: any) => ({ actor: match[1], issue: match[2] })
    },
    {
       regex: /^(.*) commented on your issue: "(.*)"$/,
       key: 'comment',
       map: (match: any) => ({ actor: match[1], issue: match[2] })
    }
  ];

  for (const p of patterns) {
    const match = message.match(p.regex);
    if (match) {
      const params = p.map(match) as any;
      // Localize status if present
      if (params.status) {
        const statusKey = params.status.toLowerCase().replace(' ', '_');
        params.status = statuses[statusKey as Status]?.label[locale as 'en' | 'am'] || params.status;
      }
      return t(`templates.${p.key}`, params);
    }
  }

  return message; // Fallback to original message
}
